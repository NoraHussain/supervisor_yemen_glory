
  String getClassOrderStrong(classOrder)
  {
      switch (classOrder)
      {
        case '1':
          return 'الحصة الأولى';

        case '2':
          return 'الحصة الثانية';

        case '3':
          return 'الحصة الثالثة';

        case '4':
          return 'الحصة الرابعة';


        case '5':
          return 'الحصة الخامسة';


        case '6':
          return 'الحصة السادسة';


        case '7':
          return 'الحصة السابعة';


        default:
          return 'أخرى';
      }
  }