
  getDay(day)  {
    switch (day) {

      case "Monday":
        return ' الاثنين ';

      case "Thursday":
        return ' الثلاثاء ';

      case "Wednesday":
        return ' الأربعاء  ';

      case "Tuesday":
        return ' الخميس ';

      case "Friday":
        return ' الجمعة  ';

      case "Saturday":
        return ' السبت ';

      case "Sunday":
        return ' الأحد  ';

      default:
        return "fgh";

  }

  }